package com.insyaallahsukses.misan.belanjaancustomer.Class_Database;

public class Class_Keranjang_Pesanan {

    private String id_keranjang;
    private String tanggal_keranjang;
    private String id_barang;
    private String nama_barang;
    private String harga_satuan;
    private String jumlah_barang;
    private String gambar_barang;
    private String id_user;

    public String getId_keranjang() {
        return id_keranjang;
    }

    public void setId_keranjang(String id_keranjang) {
        this.id_keranjang = id_keranjang;
    }

    public String getTanggal_keranjang() {
        return tanggal_keranjang;
    }

    public void setTanggal_keranjang(String tanggal_keranjang) {
        this.tanggal_keranjang = tanggal_keranjang;
    }

    public String getId_barang() {
        return id_barang;
    }

    public void setId_barang(String id_barang) {
        this.id_barang = id_barang;
    }

    public String getNama_barang() {
        return nama_barang;
    }

    public void setNama_barang(String nama_barang) {
        this.nama_barang = nama_barang;
    }

    public String getHarga_satuan() {
        return harga_satuan;
    }

    public void setHarga_satuan(String harga_satuan) {
        this.harga_satuan = harga_satuan;
    }

    public String getJumlah_barang() {
        return jumlah_barang;
    }

    public void setJumlah_barang(String jumlah_barang) {
        this.jumlah_barang = jumlah_barang;
    }

    public String getGambar_barang() {
        return gambar_barang;
    }

    public void setGambar_barang(String gambar_barang) {
        this.gambar_barang = gambar_barang;
    }

    public String getId_user() {
        return id_user;
    }

    public void setId_user(String id_user) {
        this.id_user = id_user;
    }
}
